NTES_BASE_URL = "https://ntes.indianrail.gov.in/Coreata"
NTES_QUERY_PATH = "/QueryResult?queryType=LiveTrainStatus&trainNo={train_number}&date={date}"

RAIL_YATRI_URL = "https://www.railyatri.in/api/v1/running-status?train_number={train_number}"
RUNNING_STATUS_URL = "https://runningstatus.in/api/v1/status/{train_number}"

COOKIE_HANDSHAKE_URL = "https://ntes.indianrail.gov.in/"


class ScraperHTTPError(Exception):
    """Raised when scraper encounters an HTTP error that should be propagated."""

    def __init__(self, status_code: int, message: str):
        self.status_code = status_code
        self.message = message
        super().__init__(f"HTTP {status_code}: {message}")


async def fetch_session_cookies(client: httpx.AsyncClient) -> dict:
    """Fetch session cookies from NTES homepage for authentication."""
    headers = {
        "User-Agent": random.choice(USER_AGENTS),
        "Accept": "text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8",
        "Accept-Language": "en-US,en;q=0.9",
    }
    try:
        response = await client.get(COOKIE_HANDSHAKE_URL, headers=headers, timeout=10.0)
        response.raise_for_status()
        cookies = dict(response.cookies)
        logger.info(f"Fetched {len(cookies)} session cookies from NTES.")
        return cookies
    except Exception as e:
        logger.warning(f"Cookie handshake failed: {e}")
        return {}


async def fetch_train_status(
    train_number: str, date: str, session_cookies: Optional[dict] = None
) -> Optional[TrainTelemetry]:
    """Fetch live train status from NTES with cookie handshake."""
    headers = {
        "User-Agent": random.choice(USER_AGENTS),
        "Accept": "application/json, text/javascript, */*; q=0.01",
        "Accept-Language": "en-US,en;q=0.9",
        "X-Requested-With": "XMLHttpRequest",
        "Origin": NTES_BASE_URL,
        "Referer": f"{NTES_BASE_URL}/",
        "Connection": "keep-alive",
    }

    url = f"{NTES_BASE_URL}{NTES_QUERY_PATH.format(train_number=train_number, date=date)}"

    async with httpx.AsyncClient() as client:
        cookies = session_cookies or await fetch_session_cookies(client)
        response = await client.get(
            url, headers=headers, cookies=cookies, timeout=10.0
        )

        if response.status_code in (403, 429):
            raise ScraperHTTPError(response.status_code, f"NTES returned {response.status_code}")

        response.raise_for_status()
        return parse_ntes_response(response.text)


async def fetch_from_railyatri(train_number: str) -> Optional[TrainTelemetry]:
    """Fallback: Fetch from RailYatri API."""
    url = RAIL_YATRI_URL.format(train_number=train_number)
    headers = {
        "User-Agent": random.choice(USER_AGENTS),
        "Accept": "application/json",
    }
    try:
        async with httpx.AsyncClient() as client:
            response = await client.get(url, headers=headers, timeout=10.0)
            response.raise_for_status()
            data = response.json()
            return TrainTelemetry(
                train_number=train_number,
                current_lng=float(data.get("longitude", 0)),
                current_lat=float(data.get("latitude", 0)),
                delay_minutes=int(data.get("delay", 0)),
                bearing=int(data.get("bearing", 0)),
            )
    except Exception as e:
        logger.warning(f"RailYatri fallback failed for {train_number}: {e}")
        return None


async def fetch_from_runningstatus(train_number: str) -> Optional[TrainTelemetry]:
    """Fallback: Fetch from RunningStatus API."""
    url = RUNNING_STATUS_URL.format(train_number=train_number)
    headers = {
        "User-Agent": random.choice(USER_AGENTS),
        "Accept": "application/json",
    }
    try:
        async with httpx.AsyncClient() as client:
            response = await client.get(url, headers=headers, timeout=10.0)
            response.raise_for_status()
            data = response.json()
            return TrainTelemetry(
                train_number=train_number,
                current_lng=float(data.get("lng", 0)),
                current_lat=float(data.get("lat", 0)),
                delay_minutes=int(data.get("delay", 0)),
                bearing=int(data.get("heading", 0)),
            )
    except Exception as e:
        logger.warning(f"RunningStatus fallback failed for {train_number}: {e}")
        return None


async def fetch_with_fallback(
    train_number: str, date: str, session_cookies: Optional[dict] = None
) -> Optional[TrainTelemetry]:
    """Fetch train status with fallback endpoints."""
    result = await fetch_train_status(train_number, date, session_cookies)
    if result and result.current_lng and result.current_lat:
        return result

    logger.info(f"NTES failed for {train_number}, trying RailYatri fallback...")
    result = await fetch_from_railyatri(train_number)
    if result and result.current_lng and result.current_lat:
        return result

    logger.info(f"RailYatri failed for {train_number}, trying RunningStatus fallback...")
    result = await fetch_from_runningstatus(train_number)
    return result
